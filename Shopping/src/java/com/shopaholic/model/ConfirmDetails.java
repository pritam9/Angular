package com.shopaholic.model;

/**
 *
 * @author Pritam
 */
public class ConfirmDetails {
    private String address,city,state,zip,shippingType,creditCard,holderName,cvv,validFrom,validTill;

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getShippingType() {
        return shippingType;
    }

    public void setShippingType(String shippingType) {
        this.shippingType = shippingType;
    }

    public String getCreditCard() {
        return creditCard;
    }

    public void setCreditCard(String creditCard) {
        this.creditCard = creditCard;
    }

    public String getHolderName() {
        return holderName;
    }

    public void setHolderName(String holderName) {
        this.holderName = holderName;
    }

    public String getCvv() {
        return cvv;
    }

    public void setCvv(String cvv) {
        this.cvv = cvv;
    }

    public String getValidFrom() {
        return validFrom;
    }

    public void setValidFrom(String validFrom) {
        this.validFrom = validFrom;
    }

    public String getValidTill() {
        return validTill;
    }

    public void setValidTill(String validTill) {
        this.validTill = validTill;
    }

    @Override
    public String toString() {
        return "ConfirmDetails{" + "address=" + address + ", city=" + city + ", state=" + state + ", zip=" + zip + ", shippingType=" + shippingType + ", creditCard=" + creditCard + ", holderName=" + holderName + ", cvv=" + cvv + ", validFrom=" + validFrom + ", validTill=" + validTill + '}';
    }
    
    
    
    
    public ConfirmDetails() {
    }
    
    
}
