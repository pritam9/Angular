package com.shopaholic.model;

import java.io.Serializable;
import java.sql.Date;

/**
 *
 * @author Pritam
 */
public class Appointment implements Serializable{
    //appointment_id primary key and subscripton_id foreign_key
    int appointment_id,group_no,subscription_id;
    String userid,start_time,end_time,subject_code;
    boolean isGroupAssignment;
    Date a_date;

    public Date getA_date() {
        return a_date;
    }

    public void setA_date(Date a_date) {
        this.a_date = a_date;
    }

    public String getSubject_code() {
        return subject_code;
    }

    public void setSubject_code(String subject_code) {
        this.subject_code = subject_code;
    }

    public int getAppointment_id() {
        return appointment_id;
    }

    public void setAppointment_id(int appointment_id) {
        this.appointment_id = appointment_id;
    }

    public int getGroup_no() {
        return group_no;
    }

    public void setGroup_no(int group_no) {
        this.group_no = group_no;
    }

    public int getSubscription_id() {
        return subscription_id;
    }

    public void setSubscription_id(int subscription_id) {
        this.subscription_id = subscription_id;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getStart_time() {
        return start_time;
    }

    public void setStart_time(String start_time) {
        this.start_time = start_time;
    }

    public String getEnd_time() {
        return end_time;
    }

    public void setEnd_time(String end_time) {
        this.end_time = end_time;
    }

    public boolean isIsGroupAssignment() {
        return isGroupAssignment;
    }

    public void setIsGroupAssignment(boolean isGroupAssignment) {
        this.isGroupAssignment = isGroupAssignment;
    }
    
    
}
